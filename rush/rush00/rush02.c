#include <unistd.h>

void	rush02(int x, int y)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (j < y)
	{
		while (i < x)
		{
			if ((i == 0 && j == 0) || (i == x - 1 && j == 0))
				write(1, "A", 1);
			else if ((i == 0 && j == y - 1) || (i == x - 1 && j == y - 1))
				write(1, "C", 1);
			else if ((i == 0 || i == x - 1) || (j == 0 || j == y - 1))
				write(1, "B", 1);
			else
				write(1, " ", 1);
			if (i == x - 1)
				write(1, "\n", 1);
			i++;
		}
		j++;
		i = 0;
	}
}
