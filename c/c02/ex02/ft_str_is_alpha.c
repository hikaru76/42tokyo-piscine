int		ft_str_is_alpha(char *str)
{
	int		i;
	int		flag;
	char	c;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
