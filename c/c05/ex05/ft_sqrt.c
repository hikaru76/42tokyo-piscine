#include <stdio.h>

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0 || nb > (46340 * 46340))
		return (0);
	while (i * i <= nb)
	{
		i++;
		if (i * i == nb)
			return (i);
	}
	return (0);
}
