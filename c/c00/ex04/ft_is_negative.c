#include <unistd.h>

void	ft_is_negative(int n)
{
	char s;

	if (n < 0)
	{
		s = 'N';
	}
	else if (n >= 0)
	{
		s = 'P';
	}
	else
	{
		s = 'P';
	}
	write(1, &s, 1);
}
