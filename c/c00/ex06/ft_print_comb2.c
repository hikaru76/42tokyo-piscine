#include <unistd.h>

void	loop(char a, char b, char c, char d)
{
	while (d <= '9')
	{
		if ((a < c) || (a == c && b < d))
		{
			write(1, &a, 1);
			write(1, &b, 1);
			write(1, " ", 1);
			write(1, &c, 1);
			write(1, &d, 1);
			if (!(a == '9' && b == '8' && c == '9' && d == '9'))
			{
				write(1, ",", 1);
				write(1, " ", 1);
			}
		}
		d++;
	}
}

void	ft_print_comb2(void)
{
	char a[4];

	a[0] = '0';
	a[1] = '0';
	a[2] = '0';
	a[3] = '1';
	while (a[0] <= '9')
	{
		while (a[1] <= '9')
		{
			while (a[2] <= '9')
			{
				loop(a[0], a[1], a[2], a[3]);
				a[2]++;
				a[3] = '0';
			}
			a[1]++;
			a[2] = '0';
		}
		a[0]++;
		a[1] = '0';
	}
}
